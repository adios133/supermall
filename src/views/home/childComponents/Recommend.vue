<template>
  <div class="recommend">
    <div v-for="(item, index) in recommend" :key="index" class="recommend-item">
      <img
        :src="item.image"
        :alt="item.title"
        class="recommend-item-img"
        @load="imageLoad"
      />
      <div class="recommend-item-title">{{ item.title }}</div>
    </div>
  </div>
</template>

<script>
export default {
  name: "",
  props: {
    recommend: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      isLoad: false,
    };
  },
  methods: {
    imageLoad() {
      if (!this.isLoad) {
        this.$emit("recommendLoad");
        this.isLoad = true;
      }
    },
  },
};
</script>

