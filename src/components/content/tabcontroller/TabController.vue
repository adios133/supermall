<template>
  <div class='tabcontroller'>
    <div v-for="(item,index) in goodsType" 
    :key="index" 
    :class='{actived:currentIndex === index}'
    @click="tabClick(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  //import x from ''
  export default {
    props: {
      goodsType: {
        type:Array,
        default() {
          return []
        }
      }
    },
    data() {
      return {
        currentIndex:0
      }
    },
    methods: {
      tabClick(index) {
        this.currentIndex = index
        this.$emit('goodsSwitch',index)
      }
    }
  }
</script>

